package model;

/**
 *
 * @author acer
 */
public class Employee {
    private Address address;
    private Name name;

    public Employee(Address address, Name name) {
        this.address = address;
        this.name = name;
    }

    public Address getAddress() {
        return address;
    }

    public void setAddress(Address address) {
        this.address = address;
    }

    public Name getName() {
        return name;
    }

    public void setName(Name name) {
        this.name = name;
    }
    
    
}
