public class FormMD {
    private int id;
    private String formName;

    public FormMD(int id, String formName) {
        this.id = id;
        this.formName = formName;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getFormName() {
        return formName;
    }

    public void setFormName(String formName) {
        this.formName = formName;
    }
    
    
}
