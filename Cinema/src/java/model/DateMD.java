package model;

/**
 *
 * @author acer
 */
public class DateMD {
    private int date;
    private int month;
    private String day;

    public DateMD() {
    }

    public DateMD(int date, int month, String day) {
        this.date = date;
        this.month = month;
        this.day = day;
    }

    public int getDate() {
        return date;
    }

    public void setDate(int date) {
        this.date = date;
    }

    public int getMonth() {
        return month;
    }

    public void setMonth(int month) {
        this.month = month;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }
    
    
}
